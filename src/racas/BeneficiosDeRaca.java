package racas;

public class BeneficiosDeRaca {
    private Integer beneficioForca;
    private Integer beneficioDestreza;
    private Integer beneficioConstituicao;
    private Integer beneficioInteligencia;
    private Integer beneficioSabedoria;
    private Integer beneficioCarisma;

    public Integer getBeneficioForca() {
        return beneficioForca;
    }

    public void setBeneficioForca(Integer beneficioForca) {
        this.beneficioForca = beneficioForca;
    }

    public Integer getBeneficioDestreza() {
        return beneficioDestreza;
    }

    public void setBeneficioDestreza(Integer beneficioDestreza) {
        this.beneficioDestreza = beneficioDestreza;
    }

    public Integer getBeneficioConstituicao() {
        return beneficioConstituicao;
    }

    public void setBeneficioConstituicao(Integer beneficioConstituicao) {
        this.beneficioConstituicao = beneficioConstituicao;
    }

    public Integer getBeneficioInteligencia() {
        return beneficioInteligencia;
    }

    public void setBeneficioInteligencia(Integer beneficioInteligencia) {
        this.beneficioInteligencia = beneficioInteligencia;
    }

    public Integer getBeneficioSabedoria() {
        return beneficioSabedoria;
    }

    public void setBeneficioSabedoria(Integer beneficioSabedoria) {
        this.beneficioSabedoria = beneficioSabedoria;
    }

    public Integer getBeneficioCarisma() {
        return beneficioCarisma;
    }

    public void setBeneficioCarisma(Integer beneficioCarisma) {
        this.beneficioCarisma = beneficioCarisma;
    }
}
